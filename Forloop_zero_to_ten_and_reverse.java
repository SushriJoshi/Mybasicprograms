package Methods;

public class Forloop_zero_to_ten_and_reverse 
{
 
	public static void main(String[] args) 
	{
	
		for(int i=0;i<=10;i++)
			System.out.println(i);   //i<11   print from 0 to 10
		
		for(int i=1;i<4;i++)    //we can also write the condition as i<4
			System.out.println("Sushri");
		
		for(int i=10;i>=1;i--)     //Print from 10 to 1
			System.out.println(i);
	}
}
