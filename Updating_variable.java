package Methods;

public class Updating_variable 
{
  public static void main(String[] args) 
  {
	int age=30;
	age=35;
	System.out.println(age);
	
	char a='M';
	a='L';
	System.out.println(a);
	
	String name="sushri";
	name="Sushri joshi";
	System.out.println(name);
	
	boolean b=true;
	b=false;
	System.out.println(b);
	
	double d=36.58;
	d=39.89654;
	System.out.println(d);
	
	float f=3.222555487f;
	f=6.325698f;
	System.out.println(f);
	
  }
	
	
}
