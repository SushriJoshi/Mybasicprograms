package Methods;

public class Global_defaultvalues 
{
	
	static int c;
	static long d;
	static double e;
	static boolean f;
	static char m;
	static String g;
	
	
	public static void main(String[] args) 
	{
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(m);
		System.out.println(g);
		
		
	}
		
		
	
	
	

}
