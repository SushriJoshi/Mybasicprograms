package Methods;

public class MethodOverloading_Concept 
{

	public static void main(String[] args) 
	{
		
		System.out.println("Method Overloading");
		System.out.println("Developing multiple methods with the same name but variation with the argument list is called method overloading.");
		System.out.println("Variation in the argument list indicates-:");
		System.out.println("-Number of arguments.");
		System.out.println("-Order of arguments.");
		System.out.println("-Different types of arguments.");
		System.out.println("Arguments means parameters.");
		
		System.out.println("We can overload the static method.");
		System.out.println("We can overload the non static method.");
		System.out.println("We can overload the constructors.");
		System.out.println("We can overload the main method.");
		System.out.println("all the methods can be overloaded");
		
	}
	
}
